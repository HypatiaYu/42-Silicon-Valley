int		ft_atoi(const char *str)
{
	int sign;
	int j;

	sign = 1;
	j = 0;
	while (*str && (*str == ' ' || *str == '\n' || *str == '\t' ||
				*str == '\v' || *str == '\r' || *str == '\f'))
		++str;
	if (*str == '\0')
		return (0);
	if (*str == '-')
		sign = -1;
	if ((*str == '-') || (*str == '+'))
		++str;
	while ((*str <= '9') && (*str >= '0'))
	{
		j = j * 10 + *str - '0';
		++str;
	}
	if (j > 2147483647)
		return (-0);
	return (j * sign);
}
